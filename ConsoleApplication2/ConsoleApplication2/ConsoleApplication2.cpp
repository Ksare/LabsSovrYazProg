// Подключаем библиотеку для ввода/вывода (cin, cout)
#include <iostream>

using namespace std;

/**
 * Функция для возведения числа в степень
 * @param x - основание степени (число, которое возводим в степень)
 * @param n - показатель степени (в какую степень возводим)
 * @return - результат возведения x в степень n
 */
int power(int x, int n)
{
    int p = 1;  // Инициализируем результат = 1 (так как любое число в степени 0 = 1)

    // Цикл выполняется n раз (от 0 до n-1)
    // Например: если n=3, то умножим x * x * x
    for (int i = 0; i < n; i++)
        p *= x;  // Каждый раз умножаем текущий результат на x (p = p * x)

    return p;    // Возвращаем полученный результат
}


int main()
{
    int base;  // Переменная для хранения основания степени

    // Выводим приглашение для ввода 
    cout << "Enter base: ";

    // Считываем число, введенное пользователем с клавиатуры
    // и сохраняем его в переменную base
    cin >> base;

    // Цикл для вычисления степеней от 0 до 9
    // i - это показатель степени (0, 1, 2, 3, ..., 9)
    for (int i = 0; i < 10; i++)
    {
        // Вызываем функцию power с параметрами:
        // основание = base, показатель = i
        // Результат сохраняем в переменную k
        int k = power(base, i);

        // Выводим результат: показатель степени и вычисленное значение
        cout << "Power " << i << ", result = " << k << endl;
    }

    return 0;  // Возвращаем 0, сигнализируя операционной системе об успешном завершении
}